# 3 グレンジャー因果性 ----------------------------------------------------------

# ＜ポイント＞
# - 1つの系列の将来を予測するにあたり、別の系列の過去の値が役に立つ場合はグレンジャー因果性があるという
#   --- ベクトル自己回帰モデル(VER)のような時系列分析における予測を目的とする場合に有用
#   --- 2つの系列における因果関係については何も語っていない（統計的因果推論とは別物）



# 1 統計的因果推論とは ---------------------------------------------------------

# ＜ポイント＞
# - 実験データや観察データから得られた不完全な情報をもとに、事象の因果効果を統計的に推定していくこと
#   --- 原因Aが結果Bのに対してもたらす効果を測定する
#   --- 効果をもたらした原因を特定するには、原因をもたらす効果を特定する必要がある


